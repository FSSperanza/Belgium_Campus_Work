function grade(result)
{
if (result >= 90)
{
return 'A';
}
else if (result >=80 && result <90)
{
return 'B';
}
else if (result >=70 && result <80)
{
return 'C';
}
else if (result >=60 && result <70)
{
return 'D';
}
else if (result >=50 && result <60)
{
return 'E';
}
else if (result < 50)
{
return 'F';
}
else 
return 'Invalid Result'
}
